class Shoe
  attr_accessor :brand, :color, :size, :material, :condition
  
  def initialize(brand)
  end
  
  def brand
    @brand = brand
  end
  
  def color
    @color = color
  end
  
  def size
    @size = size
  end
  
  def material
    @material = material
  end
  
  def condition
    @condition = condition
  end
  
end